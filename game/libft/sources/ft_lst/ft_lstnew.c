#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*s1;

	s1 = (t_list *)malloc(sizeof(t_list));
	if (s1 == NULL)
		return (0x0);
	s1->content = content;
	s1->next = NULL;
	return (s1);
}
